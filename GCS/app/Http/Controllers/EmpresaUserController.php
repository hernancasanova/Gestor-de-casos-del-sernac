        //
        $empresas=DB::table('empresa_user')
            ->leftJoin('empresas','empresa_user.empresa_id','=','empresas.id')
            ->where('user_id','=',$id)
            ->select('empresas.nombre_empresa','empresas.id')
            ->get();
        return response([
            'empresas'        => $empresas,
            'status_code' => 200,
